#!/usr/bin/env node
/**
 * Syncs package.json versions into docs and CLI metadata.
 * Run after `changeset version` (via `bun run version`).
 */
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const packagesDir = join(root, "packages");
const docsDir = join(root, "apps/docs");

/** Canonical version for the whole EchoJS ecosystem (meta-package). */
const ECOSYSTEM_VERSION_PACKAGE = "@echojs-ecosystem/framework";

const readPackageJson = (path) => JSON.parse(readFileSync(path, "utf8"));
const writePackageJson = (path, data) => writeFileSync(path, `${JSON.stringify(data, null, 2)}\n`, "utf8");

const readEcosystemVersion = () => {
  const path = join(docsDir, "src/core/content/ecosystem-version.generated.ts");
  try {
    const source = readFileSync(path, "utf8");
    const match = source.match(/export const ECOSYSTEM_VERSION = "([^"]+)"/);
    return match?.[1] ?? null;
  } catch {
    return null;
  }
};

const readDocVersionHistory = () => {
  const path = join(docsDir, "src/core/content/doc-version-history.generated.ts");
  try {
    const source = readFileSync(path, "utf8");
    /** @type {Array<{ id: string; label: string; href?: string; disabled?: boolean }>} */
    const history = [];
    const entryPattern =
      /\{\s*id:\s*"([^"]+)"\s*,\s*label:\s*"([^"]+)"([^}]*)\}/g;

    for (const match of source.matchAll(entryPattern)) {
      const tail = match[3] ?? "";
      history.push({
        id: match[1],
        label: match[2],
        ...(tail.includes('href: "') ? { href: tail.match(/href:\s*"([^"]+)"/)?.[1] } : {}),
        ...(tail.includes("disabled: true") ? { disabled: true } : {}),
      });
    }

    return history;
  } catch {
    return [];
  }
};

const collectVersions = () => {
  /** @type {Record<string, string>} */
  const versions = {};

  for (const entry of readdirSync(packagesDir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    const pkgDir = join(packagesDir, entry.name);
    const pkg = readPackageJson(join(pkgDir, "package.json"));
    if (pkg.name) versions[pkg.name] = pkg.version;
  }

  return versions;
};

const docVersionId = (version) => {
  const [major = "0", minor = "0"] = version.split(".");
  return `${major}.${minor}`;
};

const formatEcosystemVersionTs = (versions, ecosystemVersion) => {
  const sortedEntries = Object.entries(versions).sort(([a], [b]) => a.localeCompare(b));
  const lines = sortedEntries.map(([name, version]) => `  "${name}": "${version}",`);

  return `/** Generated by scripts/sync-package-versions.mjs — do not edit manually. */
export const ECOSYSTEM_VERSION = "${ecosystemVersion}";

export const packageVersions = {
${lines.join("\n")}
} as const;

export type PackageName = keyof typeof packageVersions;

export const getPackageVersion = (name: string): string | undefined =>
  name in packageVersions ? packageVersions[name as PackageName] : undefined;
`;
};

const formatDocVersionHistoryTs = (history) => {
  const body = history.length
    ? history
        .map(
          (entry) =>
            `  { id: "${entry.id}", label: "${entry.label}"${entry.href ? `, href: "${entry.href}"` : ""}${entry.disabled ? ", disabled: true" : ""} },`,
        )
        .join("\n")
    : "";

  return `/** Generated by scripts/sync-package-versions.mjs — do not edit manually. */
export type DocVersionHistoryEntry = {
  id: string;
  label: string;
  href?: string;
  disabled?: boolean;
};

export const DOC_VERSION_HISTORY: readonly DocVersionHistoryEntry[] = [
${body}
];
`;
};

const syncArchitectCliVersion = (version) => {
  const path = join(root, "packages/architect/src/cli/version.ts");
  writeFileSync(path, `export const version = "${version}"\n`, "utf8");
};

const syncDocsPackageVersion = (version) => {
  const path = join(docsDir, "package.json");
  const pkg = readPackageJson(path);
  pkg.version = version;
  writePackageJson(path, pkg);
};

const appendRetiredDocVersion = (history, previousVersion) => {
  if (!previousVersion || previousVersion === "0.0.0") return history;

  const id = docVersionId(previousVersion);
  if (history.some((entry) => entry.id === id)) return history;

  return [
    ...history,
    {
      id,
      label: `v${previousVersion}`,
      disabled: true,
    },
  ];
};

const main = () => {
  const versions = collectVersions();
  const ecosystemVersion =
    versions[ECOSYSTEM_VERSION_PACKAGE] ??
    versions["@echojs-ecosystem/reactivity"] ??
    "0.0.0";

  const previousVersion = readEcosystemVersion();
  const history = appendRetiredDocVersion(readDocVersionHistory(), previousVersion);

  writeFileSync(
    join(docsDir, "src/core/content/ecosystem-version.generated.ts"),
    formatEcosystemVersionTs(versions, ecosystemVersion),
    "utf8",
  );

  writeFileSync(
    join(docsDir, "src/core/content/doc-version-history.generated.ts"),
    formatDocVersionHistoryTs(history),
    "utf8",
  );

  syncDocsPackageVersion(ecosystemVersion);

  if (versions["@echojs-ecosystem/architect"]) {
    syncArchitectCliVersion(versions["@echojs-ecosystem/architect"]);
  }

  console.log(
    `Synced ${Object.keys(versions).length} package versions (ecosystem: v${ecosystemVersion}${previousVersion && previousVersion !== ecosystemVersion ? `, was v${previousVersion}` : ""}).`,
  );
};

main();
